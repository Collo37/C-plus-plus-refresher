#include <iostream>
#include <cmath>

using namespace std;

int main() {

    cout << pow(2, 3) << endl;
    cout << sqrt(4) << endl;
    cout << round(22/7) << endl;
    cout << ceil(5.5) << endl;
    cout << floor(5.5) << endl;
    cout << fmax(3, 22) << endl;
    cout << fmin(3, 22) << endl;

    return 0;
}