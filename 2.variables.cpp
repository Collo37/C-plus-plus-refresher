#include <iostream>

using namespace std;

// GLOBAL VARIABLES
int sum;

int main() {
    // LOCAL VARIABLES
    int a, b;

    // INSTRUCTIONS
    a = 5;
    b = 2;
    sum = a - b;

    // Variable initialzation
    int age = 25; // c-like initialization
    float balance (23.55);

    cout << "Sum: " << sum << "\n";
    cout << "Age: " << age << "\n";
    cout << "Balance: " << balance << "\n";
    return 0;
}